- (IBAction)sendAction:(UIButton *)sender {
    
    
    [self.textField endEditing:YES];
    NSString *bundleId = [[NSBundle mainBundle] bundleIdentifier];
    NSString *version = [[[NSBundle mainBundle]infoDictionary] objectForKey:@"CFBundleShortVersionString"];
//    NSString* deviceName = [[UIDevice currentDevice] systemName];
    NSString* phoneVersion = [@"iOS" stringByAppendingString:[[UIDevice currentDevice] systemVersion]];
//    NSString* phoneModel = [[UIDevice currentDevice] name];
    NSString *identifierStr = [[[UIDevice currentDevice] identifierForVendor] UUIDString];
    NSString *phoneType = [self getDeviceName];
    NSString *vcode = self.textField.text;
    if (vcode == nil || [vcode isEqualToString:@""]) {
        JCAlertController *alert = [JCAlertController alertWithTitle:@"提示" message:@"请输入验证码"];
        [alert addButtonWithTitle:@"确定" type:JCButtonTypeNormal clicked:nil];
        [self presentViewController:alert animated:YES completion:nil];
    }
    [NetWork submitLoginWithVcode:vcode udid:identifierStr appbag:bundleId devicetype:phoneType ossystem:phoneVersion version:version success:^(NSDictionary *response) {
        NSLog(@"%@", response);
        
        if ([response[@"code"] integerValue] == 0) {
            ViewController *vc = [[UIStoryboard storyboardWithName:@"Main" bundle: nil] instantiateViewControllerWithIdentifier:@"ViewController"];
            UINavigationController *nav = [[UINavigationController alloc] initWithRootViewController:vc];
            [UIApplication sharedApplication].keyWindow.rootViewController =  nav;
            [USERDEFAULT setObject:@(1) forKey:@"isLogin"];
            [USERDEFAULT setObject:vcode forKey:@"vcode"];
        }else {
            JCAlertController *alert = [JCAlertController alertWithTitle:@"提示" message:response[@"msg"]];
            [alert addButtonWithTitle:@"确定" type:JCButtonTypeNormal clicked:nil];
            [self presentViewController:alert animated:YES completion:nil];
        }
    } failure:^(NSString *message) {
        NSLog(@"%@", message);
        JCAlertController *alert = [JCAlertController alertWithTitle:@"提示" message:message];
        [alert addButtonWithTitle:@"确定" type:JCButtonTypeNormal clicked:nil];
        [self presentViewController:alert animated:YES completion:nil];
    } ];
    
    
}



